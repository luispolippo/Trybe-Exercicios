let status = "lista";

switch (status) {
  case "aprovada":
    console.log("Parabéns! Você passou.");
    break;

  case "lista":
    console.log("Você está em nossa lista de espera.");
    break;

  case "reprovada":
    console.log("Você foi reprovado");
    break;

  default:
    console.log("Não se aplica");
}


